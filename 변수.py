# 리스트를 변수에 넣고 복사할때 

a = [1,2,3]
b = a 
a[1] = 4 
print(a)
print(b) 

# 이러면 서로 같은 리스트(값)을 가리키고 있기 때문에 서로 값이 같다. 값만 복사한 후 전혀 다르게 하고 싶을때는 다음과 같이 한다. 

a = [1,2,3]
b = a[:] #리스트 값을 전부 복사하겠다는 의미 
a[1] = 4 #값을 변경하고 

print(a)        #-> [1, 4, 3]
print(b)        #-> [1, 2, 3]

#혹은 copy를 쓰면 된다. 

from copy import copy
b = copy(a)

# 이것은 b = a[:]와 동일하다. 

print(b is a)   #-> False 서로 다름을 확인!! 



