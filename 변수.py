# 편의상 한글로 변수를 만들었다. 
# 변수명 = 저장할 값. 이런식으로 만든다. 

a = 3

# 파이썬의 변수는 객체를 사리키는 것으로 말할 수 있다.
# 객체는 뭔데? -> 객체는 우리가 지금껏 봤던 자료형을 포함해서 파이썬에서 사용되는 모든 것 들을 뜻하는 말이다. 
# 위에 코드처럼 a=3이라고 하면 3이라는 값을 가지는 정수 자요형이 자동으로 메모리에 생성된다. 
# a는 변수의 이름이며, 3이라는 정수형 객체가 저장된 메모리 위치를 가리킨다. 
# 정리하자면 변수 a는 객체가 저장된 메모리의 위치를 가리키는 레퍼런스라고도 부른다. 

a = 3
b = 3
a is b 
print(a is b)       #->True

# 두 변수가 같은곳을 가리키는 것으로 이해할 수 있다. 이처럼 3을 가리키는 변수가 2개인 경우를 레퍼런스 카운터가 2개라고 한다. 하나가 늘면 3개라고 하면된다. 

# 파이썬에는 입력한 자료형에 대한 참조 개수를 알려주는 함수가 존자한다. 이 함수를 이용해서 3이라는 정수형 객체에 참조 개수가 몇 개 있는지 알아보자. 

import sys
print(sys.getrefcount(3))   #->56  

# 56이 나오는 이유는내부적으로 3이라는 자료형을 이미 사용해서 이다. 이후 계속 3을 가리키는 변수(레퍼런스 카운터)를 늘리면 늘어난다. 

d = 3
print(sys.getrefcount(3)) #->57  

e = 3
print(sys.getrefcount(3)) #->58

